public class Carrera {
    String nombreDeCarrera;
    int duracion;
    boolean estaEnCurso;

    public Carrera(String nombreDeCarrera, int duracion, boolean estaEnCurso) {
        this.nombreDeCarrera = nombreDeCarrera;
        this.duracion = duracion;
        this.estaEnCurso = estaEnCurso;
    }

}
